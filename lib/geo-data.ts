// Match county name from GeoJSON to our county data (fuzzy)
export function matchCountyName(geoName: string, countyData: CountyData[]): CountyData | undefined {
  // Exact match first
  const exact = countyData.find((c) => c.name === geoName)
  if (exact) return exact

  // Try partial matches
  const geoLower = geoName.toLowerCase()
  return countyData.find((c) => {
    const cLower = c.name.toLowerCase()
    if (cLower === "other counties" || cLower === "other parishes" || cLower === "other boroughs") return false
    return geoLower.includes(cLower) || cLower.includes(geoLower)
  })
}

// Format large numbers for map labels
export function formatCount(n: number): string {
  if (n >= 1000000) return `${(n / 1000000).toFixed(1)}M`
  if (n >= 1000) return `${(n / 1000).toFixed(n >= 10000 ? 0 : 1)}K`
  return n.toString()
}

// Color scale for state/county fill based on listing density
export function getDensityColor(count: number, max: number): string {
  const ratio = count / max
  if (ratio < 0.05) return "#dbeafe"
  if (ratio < 0.1) return "#bfdbfe"
  if (ratio < 0.2) return "#93c5fd"
  if (ratio < 0.35) return "#60a5fa"
  if (ratio < 0.5) return "#3b82f6"
  if (ratio < 0.7) return "#2563eb"
  if (ratio < 0.85) return "#1d4ed8"
  return "#1e40af"
}
